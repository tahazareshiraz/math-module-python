def convert_celsius_to_fahrenheit(c):
    return c * 9.0 / 5.0 + 32.0



def convert_fahrenheit_to_celsius(f):
    return (f - 32.0) * 5.0 / 9.0



def convert_celsius_to_kelvin(c):
    return c + 273.15



def convert_kelvin_to_celsius(k):
    return k - 273.15



def convert_km_to_miles(km):
    return km * 0.621371



def convert_miles_to_km(miles):
    return miles / 0.621371



def convert_kg_to_pounds(kg):
    return kg * 2.20462



def convert_pounds_to_kg(lb):
    return lb / 2.20462



def convert_degrees_to_radians(d):
    return radians(d)



def convert_radians_to_degrees(r):
    return degrees(r)

